#include<stdio.h>
void main()
{
int a[2][3];//2차원배열선언, 2행3열로 구성되고, 원소갯수는 2X3=6개
int b,c,d; //정수형변수선언
c=0; //변수초기화
a[0][0]=10;
a[0][1]=20;
a[0][2]=30; //1행3열의 원소초기화
a[1][0]=40;
a[1][1]=50;
a[1][2]=60;
for(b=0; b<2;b++) //바깥 For문-행을 위한
{
	for(d=0;d<3;d++) //안쪽 For문-열을 위한
		{
			c= c + a[b][d];
			printf("%4d",a[b][d]);
	}
	printf("\n");
}
printf("배열의합=%d\n",c);
}